#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int int_min;

	int_min = -2147483648;
	if (n == int_min)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-n, fd);
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
	else if (n >= 0 && n <= 9)
		ft_putchar_fd(n % 10 + '0', fd);
}
